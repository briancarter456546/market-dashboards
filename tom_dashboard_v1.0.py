F_NEWLY       = os.path.join(_SCRIPT_DIR, 'tom_NEWLY_working.csv')
F_SHORT_1Y    = os.path.join(_SCRIPT_DIR, 'tom_SHORT_candidates_1Y.csv')
F_SUMMARY     = os.path.join(_SCRIPT_DIR, 'tom_SUMMARY.csv')

# ---------------------------------------------------------------------------
# Vol / inverse products to pull into their own panel
# ---------------------------------------------------------------------------
VOL_TICKERS = {'UVXY', 'VXX', 'VIXY', 'VIXM', 'VXZ', 'TAIL',
               'DOG', 'HDGE', 'UNG', 'INBS', 'NBY'}

# ---------------------------------------------------------------------------
# Filtering thresholds
# ---------------------------------------------------------------------------
MIN_YEARS_CONSISTENT = 5.0   # Minimum full-history years for panel 1
MIN_BH_CONSISTENT    = 0.0   # Minimum B&H CAGR for panel 1
MIN_1Y_NEWLY         = 10.0  # Minimum 1Y outperform for panel 3
MIN_BH_SHORT         = 0.0   # Panel 4: only show assets with positive B&H


# =============================================================================

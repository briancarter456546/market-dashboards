def rows_to_list(df, cols):
    """Convert DataFrame rows to list of dicts for JSON, NaN-safe."""
    if df.empty:
        return []
    subset = df[[c for c in cols if c in df.columns]].copy()
    # Replace inf/-inf with None, then NaN with None
    subset = subset.replace([float('inf'), float('-inf')], None)
    return json.loads(subset.to_json(orient='records'))


def build_body_html(dfs, writer):
    """Build full body HTML for DashboardWriter.write()."""

    summary    = parse_summary(dfs['summary'])
    longs_df, vol_df = filter_consistent(dfs['consistent'])
    newly_df   = filter_newly(dfs['newly'])
    short_df   = filter_short(dfs['short_1y'])

    run_date = summary.get('run_date', 'N/A')
    # Shorten date if it's datetime-like
    if ' ' in str(run_date):
        run_date = str(run_date).split(' ')[0]

    parts = []

    # ---- Stat bar ----
    parts.append(writer.stat_bar([
        ('Consistent TOM',   str(len(longs_df) + len(vol_df)), 'neutral'),
        ('Newly Working',    str(len(newly_df)),                'pos'),
        ('Stopped Working',  str(summary.get('stopped', '?')), 'neg'),
        ('Last Run',         run_date,                          'neutral'),
    ]))

    # ---- Page header ----
    parts.append(writer.build_header(
        'Turn-of-Month Signal Dashboard &nbsp;&mdash;&nbsp; '
        'Long TOM &bull; Vol Timing &bull; Newly Working &bull; Short/Avoid TOM'
    ))

    # ---- Explanation note ----
    parts.append(
        '<div class="tom-note" style="padding:0 0 4px 0;margin:0 0 18px 0;">'
        'TOM window = last 5 trading days of month through first 3 of next. '
        'Outperformance = Long-TOM CAGR minus Buy-and-Hold CAGR. '
        'Rerun <code>tom_full_scan_v3.py</code> monthly to refresh signals.'
        '</div>'
    )

    # ---- Panel 1: Consistent TOM Longs ----
    longs_html = (
        '<div class="tom-table-wrap"><div id="tbl-longs"></div></div>'
    )
    parts.append(writer.section(
        f'Consistent TOM Longs &nbsp;<span style="font-weight:400;font-size:0.85em;color:#888;">'
        f'{len(longs_df)} assets &mdash; positive Full + 5Y + 1Y, &ge;5Y history, B&H &gt; 0%</span>',
        longs_html,
        hint='Click column headers to sort'
    ))

    # ---- Panel 2: Vol/Hedge Timing ----
    vol_banner = (
        '<div class="tom-vol-banner">'
        '<strong>Volatility / Hedge Timing &mdash; not tradeable longs.</strong> '
        'These products decay permanently. Their TOM outperformance means they lose '
        '<em>less</em> during month-end windows. Use as a timing signal for hedge entry, '
        'not as a directional long trade.'
        '</div>'
    )
    vol_html = vol_banner + '<div class="tom-table-wrap"><div id="tbl-vol"></div></div>'
    parts.append(writer.section(
        f'Vol & Hedge Timing &nbsp;<span style="font-weight:400;font-size:0.85em;color:#888;">'
        f'{len(vol_df)} products</span>',
        vol_html,
        hint='Reduce hedge costs during TOM window'
    ))

    # ---- Panel 3: Newly Working Watch ----
    newly_banner = (
        '<div class="tom-warn-banner">'
        '<strong>Lower conviction &mdash; emerging signals only.</strong> '
        'These had negative TOM edge historically but are positive in the last 12 months. '
        'Could be regime shift or noise. Watch before trading; confirm with 5Y data turning positive.'
        '</div>'
    )
    newly_html = newly_banner + '<div class="tom-table-wrap"><div id="tbl-newly"></div></div>'
    parts.append(writer.section(
        f'Newly Working Watch &nbsp;<span style="font-weight:400;font-size:0.85em;color:#888;">'
        f'{len(newly_df)} assets &mdash; 1Y outperform &ge;10%, negative full history</span>',
        newly_html,
        hint='Emerging regime shift — monitor, do not trade blindly'
    ))

    # ---- Panel 4: Short / Avoid TOM ----
    short_banner = (
        '<div class="tom-short-banner">'
        '<strong>Good assets, bad at month-end.</strong> '
        'These have positive long-term B&H returns but reliably drop during TOM windows. '
        'Strategy: exit longs before TOM window and re-enter after, or short during TOM if conviction is high.'
        '</div>'
    )
    short_html = short_banner + '<div class="tom-table-wrap"><div id="tbl-short"></div></div>'
    parts.append(writer.section(
        f'Short / Avoid TOM &nbsp;<span style="font-weight:400;font-size:0.85em;color:#888;">'
        f'{len(short_df)} assets &mdash; positive B&H CAGR, negative 1Y TOM return</span>',
        short_html,
        hint='Exit before TOM window; re-enter after'
    ))

    parts.append(writer.footer())

    # ---- Embed data for JS ----
    long_cols  = ['Ticker', 'Full_LongTOM_Outperform', '5Y_LongTOM_Outperform',
                  '1Y_LongTOM_Outperform', '1Y_TOM_WinRate']
    newly_cols = ['Ticker', 'Full_LongTOM_Outperform', '5Y_LongTOM_Outperform',
                  '1Y_LongTOM_Outperform', 'Regime_1Y_vs_Full']
    short_cols = ['Ticker', '1Y_BH_CAGR', '1Y_ShortTOM_CAGR',
                  '1Y_Avg_TOM_Return', '1Y_TOM_WinRate']

    data_payload = {
        'longs': rows_to_list(longs_df,  long_cols),
        'vol':   rows_to_list(vol_df,    long_cols),
        'newly': rows_to_list(newly_df,  newly_cols),
        'short': rows_to_list(short_df,  short_cols),
    }

    data_js = (
        '<script>\nwindow._TOM_DATA = '
        + json.dumps(data_payload, ensure_ascii=False)
        + ';\n</script>\n'
    )

    return '\n'.join(parts), data_js



def load_csvs():
    dfs = {}
    for key, path in [
        ('consistent', F_CONSISTENT),
        ('newly',      F_NEWLY),
        ('short_1y',   F_SHORT_1Y),
        ('summary',    F_SUMMARY),
    ]:
        if not os.path.exists(path):
            print(f'WARNING: {path} not found — panel will be empty')
            dfs[key] = pd.DataFrame()
        else:
            dfs[key] = pd.read_csv(path, encoding='utf-8')
    return dfs


def parse_summary(df):
    if df.empty:
        return {}
    row = df.iloc[0]
    return {
        'total':      int(row.get('Total_Assets', 0)),
        'consistent': int(row.get('Consistent_Performers', 0)),
        'newly':      int(row.get('Newly_Working', 0)),
        'stopped':    int(row.get('Stopped_Working', 0)),
        'run_date':   str(row.get('Run_Date', 'N/A')),
    }


def filter_consistent(df):
    if df.empty:
        return pd.DataFrame(), pd.DataFrame()
    vol_mask = df['Ticker'].isin(VOL_TICKERS)
    vol_df = df[vol_mask].copy()
    longs_df = df[
        (~vol_mask) &
        (df.get('Full_Years', pd.Series(dtype=float)) >= MIN_YEARS_CONSISTENT) &
        (df.get('Full_BH_CAGR', pd.Series(dtype=float)) > MIN_BH_CONSISTENT)
    ].copy()
    return longs_df, vol_df


def filter_newly(df):
    if df.empty:
        return df
    col = '1Y_LongTOM_Outperform'
    if col in df.columns:
        return df[df[col] >= MIN_1Y_NEWLY].copy()
    return df


def filter_short(df):
    if df.empty:
        return df
    col = '1Y_BH_CAGR'
    if col in df.columns:
        return df[df[col] > MIN_BH_SHORT].copy()
    return df


def rows_to_list(df, cols):
    """Convert DataFrame to list of dicts for JSON, NaN/inf safe."""
    if df.empty:
        return []
    available = [c for c in cols if c in df.columns]
    subset = df[available].copy()
    subset = subset.replace([float('inf'), float('-inf')], None)
    # Convert to JSON string then back — handles NaN → null cleanly
    return json.loads(subset.to_json(orient='records'))



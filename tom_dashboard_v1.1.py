VOL_TICKERS = {'UVXY', 'VXX', 'VIXY', 'VIXM', 'VXZ', 'TAIL',
               'DOG', 'HDGE', 'UNG', 'INBS', 'NBY'}

# ---------------------------------------------------------------------------
# Filtering thresholds
# ---------------------------------------------------------------------------
MIN_YEARS_CONSISTENT = 5.0
MIN_BH_CONSISTENT    = 0.0
MIN_1Y_NEWLY         = 10.0
MIN_BH_SHORT         = 0.0


